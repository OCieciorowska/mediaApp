using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using BlazorAppMediaRating;
using BlazorAppMediaRating.Data;

namespace BlazorAppMediaRating.Components.Pages
{
    public class DetailsModel : PageModel
    {
        private readonly BlazorAppMediaRating.Data.ApplicationDbContext _context;

        public DetailsModel(BlazorAppMediaRating.Data.ApplicationDbContext context)
        {
            _context = context;
        }

        public Movie Movie { get; set; } = default!;

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var movie = await _context.Movies.FirstOrDefaultAsync(m => m.Id == id);
            if (movie == null)
            {
                return NotFound();
            }
            else
            {
                Movie = movie;
            }
            return Page();
        }
    }
}
